    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
    
      
         int selected = tblCar.getSelectedRow();
        if(selected < 0){
        JOptionPane.showMessageDialog(this,"Please selected the row to be viewed");
        return;
        }
        DefaultTableModel model = (DefaultTableModel) tblCar.getModel();
        Car selectedCar = (Car) model.getValueAt(selected,0);
        selectedCar.setCity(city2.getText());
        boolean mainValid ;
        if(maintenanceValid.isSelected()){
             mainValid = true;
        }
        else{
            mainValid = false;
        }
        selectedCar.setCompany(company2.getText());
        boolean availability ;
        if(availability2.isSelected()){
             availability = true;
        }
        else{
            availability = false;
        }
        selectedCar.setAvailability(availability);
       updateStatus.setText("");//date
       Date date = new Date();  
       SimpleDateFormat formatter = new SimpleDateFormat("MM-dd-yyyy hh:mm:ss a");   
       updateStatus.setText("Last Updated on : " + date);
       selectedCar.setDateT(date);
        refreshTable();
    }//GEN-LAST:event_btnSaveActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
         ArrayList<Date> dateList = new ArrayList<Date>();
         SimpleDateFormat formatter = new SimpleDateFormat("MM-dd-yyyy hh:mm:ss a");
        for (Car car: history.getHistory()) {
            
            if(car.getDateT() != null){
            dateList.add(car.getDateT());
            }
        }
    if(dateList.size()>0){
       String maxDate = formatter.format(Collections.max(dateList));
        updateRefresh.setText(maxDate);
    }
    else{
        updateRefresh.setText("Never Updated");
    }
      
        
    }//GEN-LAST:event_jButton2ActionPerformed

