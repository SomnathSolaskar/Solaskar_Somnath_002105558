    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        int selected = tblCar.getSelectedRow();
        if(selected < 0){
        JOptionPane.showMessageDialog(this,"Please selected the row to be deleted");
        return;
        }
        DefaultTableModel model = (DefaultTableModel) tblCar.getModel();
        Car selectedCar = (Car) model.getValueAt(selected,0);
        history.deleteCar(selectedCar);
         JOptionPane.showMessageDialog(this,"Car Record Deleted");
         populateTable();
        
    }//GEN-LAST:event_btnDeleteActionPerformed

