  public int validateInput(){
        int flag = 0;
         if(name1.getText().equals("")){
             flag = 1;
            JOptionPane.showMessageDialog(null,"Name is required");
        } 
         boolean isDuplicate = false;
         for(Car car: history.getHistory()){
            if(serial1.getText().equals(car.getNumberPlate())){
                isDuplicate = true;
        }
        }
          if(serial1.getText().equals("")){
            flag = 1;
              JOptionPane.showMessageDialog(null,"Serial Number is required");
        } 
          if(isDuplicate == true){
            flag = 1;
              JOptionPane.showMessageDialog(null,"Please enter a unique serial number", "Error", JOptionPane.ERROR_MESSAGE);
        } 
          if (manufacturer1.getText().equals("")) {
              flag = 1;
              JOptionPane.showMessageDialog(null, "Manufacturer is required");
            }
          boolean valid = Pattern.matches("(?:(?:19|20)[0-9]{2})$",manufacturingYear1.getText());//1900-2099 //2030
          boolean check = true;
          if(valid){
          int date = Integer.parseInt(manufacturingYear1.getText());
          if(date>2021 || date<2007){
              check = false;
          }
          }
          else{
          check = true;
          }
          if (manufacturingYear1.getText().equals("") || check == false) {
              flag = 1;
              JOptionPane.showMessageDialog(null, "Please enter a valid Manufacturing year from 2007 - 2021", "Error", JOptionPane.ERROR_MESSAGE);
            }
            if(company1.getText().equals("")){
                flag =1;
            JOptionPane.showMessageDialog(null,"Company name is required");
        } 
            if(city1.getText().equals("")){
                flag =1;
            JOptionPane.showMessageDialog(null,"City is required");
        } 
            if(model1.getText().equals("")){
                flag =1;
            JOptionPane.showMessageDialog(null,"Please enter a valid Model number");
        } 
  
            return flag;
    }
