    private void btnSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSubmitActionPerformed
        // TODO add your handling code here:
        
        int res = validateInput();
        if(res == 0){
        String name = name1.getText();
        String manufacturer = manufacturer1.getText();
        String serial = serial1.getText();
        String city = city1.getText();
        String model = model1.getText();
        int manYear = Integer.parseInt(manufacturingYear1.getText());
        String company = company1.getText();
        int seats = Integer.parseInt(combo.getSelectedItem().toString());
        boolean mainValid ;
        if(maintenanceValid.isSelected()){
             mainValid = true;
        }
        else{
            mainValid = false;
        }
        boolean availability ;
        if(available1.isSelected()){
             availability = true;
        }
        else{
            availability = false;
        }
        
        Car car = history.addCar();
        car.setCarName(name);
        car.setManufacturer(manufacturer);
        car.setNumberPlate(serial);
        car.setCity(city);
        car.setModelNumber(model);
        car.setManufacturingYear(manYear);
        car.setMaintenanceExpiry(mainValid);
        car.setCompany(company);
        car.setSeats(seats);
        car.setAvailability(availability);
        car.setDateTime("");
        
        JOptionPane.showMessageDialog(this,"New Car Record Added");
        
        name1.setText("");
        manufacturer1.setText("");
        serial1.setText("");
        city1.setText("");
        model1.setText("");
        manufacturingYear1.setText("");
        maintenanceValid.setSelected(false);
        company1.setText("");
        available1.setSelected(false);
        combo.setSelectedItem("5");
        }
        
        
    }//GEN-LAST:event_btnSubmitActionPerformed

